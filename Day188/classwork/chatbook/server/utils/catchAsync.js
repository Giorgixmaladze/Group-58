
// შეიქმნა ფუნქცია სახელად catchAsync რომელიც იქღებს ერთ არგუმენტს(ასინქრონულ ფუნქციას). მისი დანიშნულებაა ასინქრონულ ფუნქციაში წარმოქმნილი ერორი დაიჭიროს,პრაქტიკულია ისეთ დროს სადა ხშირად გვიწევს ასინქრონული ფუნქციების გამოყენება და try და catchს თუ გვინდა რომ ავარიდოთ თავი
const catchAsync = fn =>{
    return (req,res,next) =>{
        fn(req,res,next).catch(next)
    }
}


module.exports = catchAsync